{
  ER			error;
  struct posix_request	req;
  struct posix_response	res;
  struct psc_pipe *args = (struct psc_pipe *)argp;

  req.param.par_pipe.pipeid[0] = args->pipeid[0];
  req.param.par_pipe.pipeid[1] = args->pipeid[1];

  error = _make_connection(PSC_PIPE, &req, &res);
  if (error != E_OK)
    {
      /* What should I do? */
    }

  else if (res.errno)
    {
      ERRNO = res.errno;
      return (-1);
    }

  return (res.status);
}
